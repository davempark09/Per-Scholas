package Labs;

public class ForLoop1 {
	public static void main(String[] args) {
		
//		Question 1:
		for (int i=1; i<=10; i++) {
			System.out.print(i);
		}
	
//		Question 2:
		for(int i=1;i<=10;i+=2) {
			System.out.println("David");
		}
		
//		Question 3:
		for(int i=1;i<=15;i++) {
			System.out.print("*");
		}
		
//		Question 4:
		for(int i=1;i<=10;i+=2) {
			System.out.print(i + ",");
		}
		System.out.println("");
		for(int y=2;y<=10;y+=2) {
			System.out.print(y + ",");
		}
		
	}
}
